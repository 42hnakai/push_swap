#include "push_swap.h"

void	rb(int *ary_b)
{
	int i = 0;
	int x;
	x = ary_b[0];
	while (ary_b[i + 1] != '\0')
	{
		ary_b[i] = ary_b[i + 1];
		i++;
	}
	ary_b[i] = x;
	ary_b[i + 1] = '\0';
	printf("rb\n");
}
